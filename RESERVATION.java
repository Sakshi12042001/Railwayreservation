    public void LoadStartplace()
    {
        
        try{
            
            pst = (PreparedStatement) Con.prepareStatement("SELECT source FROM traindetails");
            rs = pst.executeQuery();
            txtsplace.removeAllItems();
            
            while(rs.next())
            {
                txtsplace.addItem(rs.getNString(1));
                
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
    
    public void LoadEndplace()
    {
        
        try{
            
            pst = (PreparedStatement) Con.prepareStatement("SELECT Destination FROM traindetails");
            rs = pst.executeQuery();
            txteplace.removeAllItems();
            
            while(rs.next())
            {
                txteplace.addItem(rs.getNString(1));
                
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
    
    
    public void Load()
    {
        String splace = txtsplace.getSelectedItem().toString();
        String eplace = txteplace.getSelectedItem().toString();
       
        try {
            pst = (PreparedStatement) Con.prepareStatement("SELECT * FROM traindetails where source = ? and Destination = ?");
            pst.setString(1, splace);
             pst.setString(2, eplace);
             rs = pst.executeQuery();
             
             if( rs.next()== true)
             {
                txtno.setText(rs.getString(1));
                txtname.setText(rs.getString(2));
                txtprice.setText(rs.getString(5));
             }
             else{
                
                  JOptionPane.showMessageDialog(this,"Record not found"); 
             }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
    
    
    
    
    
    
    
    
