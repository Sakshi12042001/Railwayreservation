    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        String pno = txtpno.getText();
        String splace = txtsplace.getSelectedItem().toString();
        String eplace = txteplace.getSelectedItem().toString();
        String tno = txtno.getText();
        String tname = txtname.getText();
        String price = txtprice.getText();
        SimpleDateFormat date_form = new SimpleDateFormat("yyy-MM-dd");
        String date = date_form.format(txtdate.getDate());     
        String tickets = txttickets.getText();
        String total = txttotal.getText();
        
        try {
            pst =  (PreparedStatement) Con.prepareStatement("insert into reservation(pno,splace ,eplace,tno,tname,price,date,ticket,	total)values(?,?,?,?,?,?,?,?,?)");
            pst.setString(1,pno );
            pst.setString(2,splace );
            pst.setString(3,eplace) ;
            pst.setString(4,tno );
            pst.setString(5,tname);
            pst.setString(6,price);
            pst.setString(7,date);
            pst.setString(8,tickets);
            pst.setString(9,total);
            
            int k = pst.executeUpdate();
            
            if (k==1)
            {
                JOptionPane.showMessageDialog(this, "Record Adddedddd");
                
            }
            else
            {
              JOptionPane.showMessageDialog(this, "Record failed");  
            }
           
            
        } catch (SQLException ex) {
            Logger.getLogger(addtrain.class.getName()).log(Level.SEVERE, null, ex);
        }
    
        
    }                                        

